import { useState, useEffect } from "react";
import { Button } from "@/components/ui/button";
import { Card, CardContent, CardHeader, CardTitle } from "@/components/ui/card";
import { supabase } from "@/integrations/supabase/client";
import { useAuth } from "@/hooks/use-auth";
import { Navigate } from "react-router-dom";
import { MessageSquare, Loader2, Shield } from "lucide-react";
import { toast } from "sonner";

export default function Auth() {
  const { isAuthenticated, loading } = useAuth();
  const [isSigningIn, setIsSigningIn] = useState(false);

  useEffect(() => {
    console.log('üîÑ Page Auth mont√©e');
    
    // Handle OAuth callback if present
    const handleAuthCallback = async () => {
      console.log('üîç V√©rification session apr√®s callback...');
      
      const { data, error } = await supabase.auth.getSession();
      console.log('üìä Session actuelle:', { 
        hasSession: !!data.session, 
        hasUser: !!data.session?.user,
        error: error?.message 
      });
      
      if (error) {
        console.error('‚ùå Erreur session:', error);
        toast.error('Erreur de session: ' + error.message);
        return;
      }

      if (data.session?.user) {
        console.log('‚úÖ Utilisateur connect√©, cr√©ation/mise √† jour en BDD...');
        const discordUser = data.session.user.user_metadata;
        console.log('üë§ Donn√©es Discord:', discordUser);
        
        if (discordUser && discordUser.provider_id) {
          const { error: upsertError } = await supabase
            .from('users')
            .upsert({
              discord_id: discordUser.provider_id,
              username: discordUser.full_name || discordUser.name || 'Utilisateur Discord',
              role: 'STAFF',
              enterprise_id: 'default'
            }, {
              onConflict: 'discord_id'
            });

          if (upsertError) {
            console.error('üíæ Erreur cr√©ation utilisateur:', upsertError);
            toast.error('Erreur lors de la cr√©ation de l\'utilisateur');
          } else {
            console.log('‚úÖ Utilisateur cr√©√©/mis √† jour avec succ√®s');
            toast.success(`Bienvenue ${discordUser.full_name || discordUser.name} !`);
          }
        }
      }
    };

    // Check for auth state changes
    const { data: { subscription } } = supabase.auth.onAuthStateChange((event, session) => {
      console.log('üîÑ Changement d\'√©tat auth:', event, !!session);
      if (event === 'SIGNED_IN' && session) {
        console.log('üìç Redirection vers la page d\'accueil...');
        // Redirect will be handled by the Navigate component
      }
    });

    handleAuthCallback();

    return () => {
      subscription.unsubscribe();
    };
  }, []);

  const signInWithDiscord = async () => {
    try {
      setIsSigningIn(true);
      
      console.log('üîê D√©but de la connexion Discord...');
      
      const { data, error } = await supabase.auth.signInWithOAuth({
        provider: 'discord',
        options: {
          scopes: 'identify guilds'
        }
      });

      console.log('üìã R√©ponse OAuth Discord:', { data, error });

      if (error) {
        console.error('‚ùå Erreur OAuth Discord:', error);
        toast.error('Erreur de connexion Discord: ' + error.message);
      } else {
        console.log('‚úÖ Redirection Discord r√©ussie');
      }
    } catch (error: any) {
      console.error('üí• Erreur g√©n√©rale:', error);
      toast.error('Erreur: ' + error.message);
    } finally {
      setIsSigningIn(false);
    }
  };

  if (loading) {
    return (
      <div className="min-h-screen bg-background flex items-center justify-center">
        <div className="flex items-center space-x-2">
          <Loader2 className="h-6 w-6 animate-spin" />
          <span>Chargement...</span>
        </div>
      </div>
    );
  }

  if (isAuthenticated) {
    return <Navigate to="/" replace />;
  }

  return (
    <div className="min-h-screen bg-background flex items-center justify-center p-6">
      <div className="w-full max-w-md space-y-8">
        <div className="text-center space-y-4">
          <div className="mx-auto w-16 h-16 bg-primary rounded-full flex items-center justify-center">
            <Shield className="h-8 w-8 text-primary-foreground" />
          </div>
          <div>
            <h2 className="text-3xl font-bold text-primary">Panel DOT</h2>
            <p className="text-muted-foreground mt-2">
              Connectez-vous avec Discord pour acc√©der au panel
            </p>
          </div>
        </div>

        <Card>
          <CardHeader className="text-center">
            <CardTitle className="text-xl">Connexion</CardTitle>
          </CardHeader>
          <CardContent className="space-y-4">
            <Button
              onClick={signInWithDiscord}
              disabled={isSigningIn}
              className="w-full flex items-center justify-center space-x-2 bg-[#5865F2] hover:bg-[#4752C4] text-white"
              size="lg"
            >
              {isSigningIn ? (
                <Loader2 className="h-5 w-5 animate-spin" />
              ) : (
                <MessageSquare className="h-5 w-5" />
              )}
              <span>
                {isSigningIn ? 'Connexion...' : 'Se connecter avec Discord'}
              </span>
            </Button>
            
            <div className="text-center text-sm text-muted-foreground">
              <p>Votre r√¥le sera automatiquement d√©tect√©</p>
              <p>selon vos permissions Discord</p>
            </div>
          </CardContent>
        </Card>

        <div className="text-center text-xs text-muted-foreground">
          <p>¬© 2025 Panel DOT - Syst√®me de gestion multi-entreprise</p>
        </div>
      </div>
    </div>
  );
}